package Array;

public class PrintArray {
    public static void main(String[] args) {
        int[] arr={7,9,12,15,26,52,47,89};
        System.out.println("Size : "+arr.length);// To print size of array
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
